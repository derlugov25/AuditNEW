export function buildAccelerators(answers: Answers, score: ScoreResult): AcceleratorInsight[] {
  // Всегда берём topThree — для отличников фреймирование меняется на уровне PDF
  // (chip/headline/цвет), но контент всегда есть.
  return score.topThree.map((d) => ACCELERATOR_BY_KEY[d.key](answers, d));
}

