/** Главный тезис страницы вердикта — в годах здоровой жизни (модель отчёта). */
export function lifeYearsHeadline(score: ScoreResult): string {
  const y = score.yearsLifeLostTotal;
  if (y < 0.05) {
    return "Потерь здоровых лет по этой модели почти не видно";
  }
  const lines = verdictLifeYearsHeadlineLines(score);
  if (!lines) return "Потерь здоровых лет по этой модели почти не видно";
  return lines.join("\n");
}

/** Связка «годы» ↔ внутренняя шкала % для спидометра и Longy Score. */
export function lifeYearsModelNote(score: ScoreResult): string {
  const v = Math.round(score.agingVelocityPct * 10) / 10;
  const sign = v > 0 ? "+" : "";
  return `Тот же уровень в служебной шкале отчёта: ${sign}${v}% к «норме» (модель от -5 до +40%). Годы — упрощённый перевод по Li et al., 2024; не прогноз срока жизни и не диагноз.`;
}

